
print("I am a Programmer.", terminator: " ")

print("I love to play games." , terminator: " ")

print("I also like to make games.", terminator: " ")

/* 
        terminator is responsible what will be at the end of the statement

        by default it is a new line character (\n)

        but we can put anything we want manually.

        For example:

        we can keep the terminator blank (terminator: "")
        
        we can keep put dot(.) in the terminator (terminator: "")

*/