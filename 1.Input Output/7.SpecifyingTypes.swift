
var intVal: Int = 3 // here 3 is  a literal

print("value of inVal is", intVal)

var doubleVal: Double = 3.1416 // here 3.1416 is a literal

print("value of doubleVal is", doubleVal)

var charVal: Character = "X" // here "X" is a literal

print("value of charVal is", charVal)

var stringVal: String = "I am a Programmer" // here "I am a Programmer" is a literal

print(stringVal)

var isEmpty: Bool = true

print("\nThe Box is empty.")
print("This statement is:", isEmpty)

