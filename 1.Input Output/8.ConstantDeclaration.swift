/*
        declaring constants
*/
let PI = 3.1416 // here 3.1416 is a literal

/*  
        declaring constants with type specified
*/
let gravity: Double = 9.8 // here 9.8 is a literal

print(PI)
print(gravity)

// the following lines will generate error cause we can't change values of constants

// PI = 3.14
// gravity = 9.98

print(PI)
print(gravity)

/*  
        declaring constant is really easy in swift

        just use the "let" keyword for declaring constants

        example:

        let PI = 3.1416
        let gravity: Double = 9.8
*/