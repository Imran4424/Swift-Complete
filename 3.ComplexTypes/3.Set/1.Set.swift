
// sets are designed to hold unique sets of values

// create a set of integer type
var studentID : Set = [112, 114, 116, 118, 115]

// we can also declare like this
var studentID2 : Set<Int> = [112, 114, 115, 116, 118]

// we can also declare like this
var studentID3 = Set([112, 114, 115, 116, 118])

print("Student ID: \(studentID)")
print("Student ID: \(studentID2)")
print("Student ID: \(studentID3)")