
var marks = (Double(readLine()!))!

if (marks >= 80) 
{
        print("A+")
}
else if (marks >= 70 && marks < 80)
{
        print("A")
}
else if (marks >= 60 && marks < 70)
{
        print("B")
}
else if (marks >= 50 && marks < 60)
{
        print("C")
}
else if (marks >= 40 &&  marks < 50)
{
        print("D")
}
else if (marks >= 33 && marks < 40)
{
        print("E")
}
else
{
        print("F")
}